const jwt = require('jsonwebtoken');

// Validates the JWT and attaches user info to the request
const authMiddleware = (req, res, next) => {
  try {
    const token = req.header('Authorization')?.replace('Bearer ', '');

    if (!token) {
      return res.status(401).json({ error: 'No token, authorization denied' });
    }

    const decoded = jwt.verify(token, process.env.JWT_SECRET);
    req.user = decoded;
    next();
  } catch (error) {
    console.error('Auth middleware error:', error);
    res.status(401).json({ error: 'Token is not valid' });
  }
};

// Ensures the caller is an admin
const adminMiddleware = (req, res, next) => {
  if (!req.user || req.user.role !== 'admin') {
    return res.status(403).json({ error: 'Admin access required' });
  }
  next();
};

// Aliases maintained for routes that expect the older names
const isAuthenticated = authMiddleware;
const isAdmin = adminMiddleware;

module.exports = { authMiddleware, adminMiddleware, isAuthenticated, isAdmin };
